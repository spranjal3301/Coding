//! NP Hard & NP Complete Problems

//` P vs NP
//- P: Polynomial time: O(n^k) where k is a constant
//- NP: Non-deterministic Polynomial time: O(2^n)

//` NP Hard
//- A problem is NP Hard if it is reduced to another Problem them it is NP Hard problem(satisfiability problem)
// Staisfiability problem X 0-1 Knapsack problem (NP Hard)

//` NP Complete
//- If a problem is NP Hard and algorithm exists to solve it in Non-deterministic Polynomial time, then it is NP Complete